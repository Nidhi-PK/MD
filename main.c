const int selectPins[3] = {S0_Pin, S1_Pin, S2_Pin};

void selectMuxPin (int pin)
{
	for (int z=0; z<3; z++)
	{
		if (pin & (1<<z))
			HAL_GPIO_WritePin(selectpinsport, selectPins[z], GPIO_PIN_SET);
		else
			HAL_GPIO_WritePin(selectpinsport, selectPins[z], GPIO_PIN_RESET);
	}
}

